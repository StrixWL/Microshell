#include "../../includes/header.h"

int	ft_pwd(void)
{
	char	cwd[1024];

	if (getcwd(cwd, 1024))
	{
		ft_putstr_fd(cwd, 1);
		ft_putchar_fd('\n', 1);
		exit (0);
	}
	else
		exit (1);
}
